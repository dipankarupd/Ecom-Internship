
class Product {
  int id;
  String title;
  String subTitle;
  int price;
  String description;
  String category;
  String thumbNail;
  List<dynamic> sideImages;
  String createdDate;

  

  Product({
    required this.id,
    required this .title,
    required this.subTitle,
    required this.price,
    required this.description,
    required this.category,
    required this.thumbNail,
    required this.sideImages,
    required this.createdDate
  });

}